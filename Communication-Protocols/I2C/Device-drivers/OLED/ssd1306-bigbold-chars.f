align

: BB&EMIT   ( c -- )
    c>n  10 * bigbold +  dup 8 .bitrow \ First half of big char
    x y 1+ xy  1+ 8 .bitrow         \ Second half of big char
    x 0A +  y 1-  xy ;              \ x & y to new char position

: BOLD      ['] bb&emit to o-emit ;


\ Example
: BOLDDEMO  ( -- )                  \ Display thin large token set
    display-setup  bold  &page
    04 0 xy" Egel project"          \ Startup message
    0E 2 xy" Characters"            \ To line 2
    14 4 xy" by J.J.H."             \ To line 4
    key drop  &page
    4 0 do
        0 i 2* xy
        i 0D * bl +  0D 0 do  dup &emit  1+  loop  drop
    loop
    key drop  &page
    2 0 do
        0 i 4 * xy 
        6 0 do  bl &emit  82 &emit  loop
        0 i 4 * 2 + xy 
        6 0 do  81 &emit  bl &emit  loop
    loop ;

\ End ;;;
